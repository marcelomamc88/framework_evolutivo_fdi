%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%% GENERATE FAULT 2 DATA TO DATASET1 (CHANGE ALTITUDE MANEUVER) %%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% INTERACTING WITH SIMULINK MODEL FROM A MATLAB SCRIPT
% https://www.youtube.com/watch?v=sF_sjFqNFUk
% FAULT 1: VISCOUS FRICTION
% DEFAULT VALUE: 1e3
% FAULT VALUE: 1e12
% PARAMETER AFFECTED: bp

clear
clc
close all

frequency = 10; % Herts
simulation_time = 100; % Seconds
datapoints = simulation_time * frequency;

writefile = true;
file_name = 'F16_DS1_fault2_viscousfriction.csv';

flights = 150; % qty of flights to generate data

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%% DEFAULT VALUES - FAULT 2 - VISCOUS FRICTION %%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
fault_amplitude = 1e12 - 1e3; %fault value - default value => 1e12 - 1e3
fault_time = 0.1; %seconds

for i=1:flights
    
    %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
    %% Generating Random Values %%
    %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

    % random amplitude =>  +- [20, 80]
    reference = [-1, 1];
    choose = randi([1 2],1,1);
    amplitude = randi([20 80],1,1) * reference(choose);
    
    % random time_start =>  [5, 50]
    time_start = randi([5 50],1,1);
    
    % random time_end => time_start + [30,50]
    time_end = time_start + randi([30 50],1,1);
    
    %%%%%%%%%%%%%%%%%%
    %% Setup Values %%
    %%%%%%%%%%%%%%%%%%

    % ALTITUDE MANUEVER
    input.altitude.amplitude = amplitude;
    input.altitude.timestart = time_start;
    input.altitude.timeend = time_end;
    
    % FAULT BETA
    input.beta.amplitude = 0;
    input.beta.time = 0;
    
    % FAULT BP
    input.bp.amplitude = fault_amplitude;
    input.bp.time = fault_time;
    
    % FAULT C LEAK
    input.c_leak.amplitude = 0;
    input.c_leak.time = 0;
    
    % FAULT VALVE POSITION
    input.valve.fault = 0;
    input.valve.amplitude = 0;
    input.valve.time = 0;

    %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
    %% run the simulink model using the 'sim' command %%
    %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
    sim('F16_DS1_ALTITUDE_SIMULINK.slx');
    
    % extract the data generated by the simulink model
    output_arr = ans.simout_arr;
    
    %%%%%%%%%%%%%%%%%%
    %% save in file %%
    %%%%%%%%%%%%%%%%%%
    if (writefile)
        writematrix(output_arr(1:datapoints,:), file_name, 'WriteMode', 'append');
    end
end


